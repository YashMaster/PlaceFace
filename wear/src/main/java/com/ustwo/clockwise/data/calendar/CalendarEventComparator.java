package com.ustwo.clockwise.data.calendar;

import java.util.Comparator;

/**
 * Class to compare the start date of {@link com.ustwo.clockwise.data.calendar.CalendarEvent} instances.
 */
public class CalendarEventComparator implements Comparator<CalendarEvent> {
    @Override
    public int compare(CalendarEvent event1, CalendarEvent event2) {
        return event1.getStart().compareTo(event2.getStart());
    }
}